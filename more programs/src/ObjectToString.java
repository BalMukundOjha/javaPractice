
public class ObjectToString {

	public static void main(String[] args) {
		
		Integer xInteger = 50;
		String string = xInteger.toString();
		System.out.println(string);
		//------------------------------------
		
		int x = 10;
		
		Integer valueOf = Integer.valueOf(x);
		String y = valueOf.toString();
		System.out.println(y);
		
		//-------------------------------------------
		
	}

}
