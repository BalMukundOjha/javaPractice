
public class StringToPrimitive {

	public static void main(String[] args) {
		
		String string = "100";
		int parseInt = Integer.parseInt(string);
		System.out.println(parseInt);
		//----------------------------------------------
		
		String s1 = "50";
		byte parseByte = Byte.parseByte(s1);
		System.out.println(parseByte);
		//------------------------------------------------------
		
	}

}
