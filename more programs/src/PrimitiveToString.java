
public class PrimitiveToString {

	public static void main(String[] args) {
		
		
		int x = 65;
		String stringX = Integer.toString(x);
		System.out.println(stringX);
		//-----------------------------------------------------
		char ch = 'A';
		String ch1 = Character.toString(ch);
		System.out.println(ch1);
		//------------------------------------------------------
	}

}
